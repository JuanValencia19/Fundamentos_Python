# 🟢 Ejercicio 7 — Cálculo de Ganancia

# Declara:

# capital = 50000  
# cuota = 1.8  

# Calcula:

# - Ganancia total
# - Ganancia neta

# Imprime ambos resultados.

capital = 50000
couta = 1.8

ganancia_total = capital * couta
ganancia_neta = ganancia_total - capital

print(f"La ganancia total es: {ganancia_total}. mientras que la ganancia neta es: {ganancia_neta}")
