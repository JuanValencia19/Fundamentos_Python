# 🟢 Ejercicio 9 — Operación Combinada

# Pide al usuario:

# - Dos números decimales

# Calcula:

# (resultado1 + resultado2) * 2

# Imprime el resultado final.

num1 = float(input("Ingrese un numero decimal: "))
num2 = float(input("Ingrese otro numero decimal: "))

combinada = (num1 * num2) * 2

print(f"La operacion combinada es: {combinada}")