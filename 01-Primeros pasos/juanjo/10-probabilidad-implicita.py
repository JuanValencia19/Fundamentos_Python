# 🟢 Ejercicio 10 — Cálculo de Probabilidad Implícita

# Pide al usuario:

# - Una cuota decimal

# Calcula:

# probabilidad = 1 / cuota

# Imprime el resultado en formato decimal y en porcentaje.

couta_decimal = float(input("Ingrese couta decimal de la apuesta: "))

probabilidad = 1 / couta_decimal

porcentaje = probabilidad * 100

print(f"La probabilidad de la couta es: {probabilidad}. y en porcentaje seria: {porcentaje}")


