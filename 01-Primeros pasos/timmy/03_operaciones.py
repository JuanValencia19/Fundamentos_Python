'''
## 📘 Ejercicio 3 – Cálculo de Ganancia Potencial

Declarar:
capital = 100000  
cuota = 2.5  

Calcular:
- Ganancia total
- Ganancia neta
'''

capital = 100000
cuota = 2.5

calculo_total = capital * cuota
print(f"Ganancia Total: {int(calculo_total)}")

calculo_neto = calculo_total - capital
print(f"Ganancia Total: {int(calculo_neto)}")